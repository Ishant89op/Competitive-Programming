class Solution {
public:
    // Solution () {
          
    // }
    bool solve(ll x, int k) {
        if(x == 1 && k == 2) return true;
        else if(k > 1) return false;
        else return prime(x);
    }
private:
    bool prime(ll n) {
        if (n <= 1) return false;
        if (n <= 3) return true;
        if (n % 2 == 0 || n % 3 == 0) return false;
        
        ll sqrtN = sqrt(n);
        for (ll i = 5; i <= sqrtN; i += 6) {
            if (n % i == 0 || n % (i + 2) == 0) {
                return false;
            }
        }
        return true;
    }
};

